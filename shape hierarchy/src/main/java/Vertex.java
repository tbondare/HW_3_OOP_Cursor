public class Vertices {
    private int x;
    private int y;
    private int z;

    public Vertices() {}

    public Vertices(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public Vertices(int x, int y, int z) {
        this.x = x;
        this.y = y;
        this.z = z;
    }


}
